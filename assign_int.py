#Assignment operators
#  =, +=, -=, *=, similarly with every arthematic operator  

# assignment operator can't be used with print statement
# In case of assignment operators, a value will be assigned to a variable

a = 5
print("initial a value", a)
a += 15 # a= a+15 a will become 20
print("a value after add is", a)
a -= 10 # a is 20, a = a-10, a will become 10
print("a value after substraction is", a)
a *= 10 # a is 10, a = a*10, a will become 100
print("a value after mul is", a)